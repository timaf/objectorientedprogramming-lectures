package oop.thebesthummus;

public class Chickpeas {

    private String name;

    private Integer kilos;

    public Chickpeas(Integer kilos) {
        this.name = "Chickpeas";
        this.kilos = kilos;
    }

    public String getName() {
        return name;
    }

    public Integer getKilos() {
        return kilos;
    }
}
