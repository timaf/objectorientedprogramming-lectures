package oop.thebesthummus;

public class Kymun {

    private String name;

    private Integer pinches;

    public Kymun(Integer pinches) {
        this.name = "Kymun";
        this.pinches = pinches;
    }

    public String getName() {
        return name;
    }

    public Integer getPinches() {
        return pinches;
    }
}
