package oop.thebesthummus;

public class Tahina {

    private String name;

    private Integer numberOfSpoons;

    public Tahina(Integer numberOfSpoons) {
        this.name = "Tahina";
        this.numberOfSpoons = numberOfSpoons;
    }

    public String getName() {
        return name;
    }

    public Integer getNumberOfSpoons() {
        return numberOfSpoons;
    }
}
